	<li>Oral tradition - most epics were not designed to be read, they were designed to be spoken. Pneumonics - meant to be recited. They had to be easy to remember. Human beings used to be able to remember novels. Illyad circled for 500 years until someone wrote it down. </li>
	<li>Standard Elements
