<p>Biases: comics are for children, theyre about superheroes, lots of masculine fantasy, read and use as toilet paper/insulate your house. Associated with racism. Sensationalism - explosions, epic battles. </p>

<p>The Language of Comics: three languages. </p>
<ul>
	<li>English, or whatever language.</li>
	<li>Comics language - word balloons. <span class="ex">Soeaking vs. Thinking.</span> Things that only happen in comics. <span class="ex">Characters in motion with lines behind them. Lines above character - they are fuming or stinking.</span> They communicate exclusively in comics.</li>
	<li>Visual language - from understanding of visual cues. We are designed to do this unconsciously. Takes an artist to draw complicated facial expressions. </li>
</ul>

<h1>Representation and Selection</h1>
<p>Trying to convey a piece of a long line of images. Choosing one image to represent a lot of other images. <span class="ex">NBA logo. Best angle and image. </span> Choose which one is best of representing something. What happens in between, we assume. ##Motion## and ##Caricature##. Image captures everything it is. ##CAricature## Choice to include eyes, nose, ears, etc. Stephen Harper's nose. Justin Trudeau - hair. Reducing drawings to have their most representative features.</p>

<h1>Gutter</h1>
<p>Gutter - space between panels of the comic. Between text (no images) and film (all imageS). Construct some images. Things happen between panels. Your brain fills in what happened. The reader animates what happens in comics. </p>

<h1>Semiotic Structures</h1>
<p>@@Micro-semiotic structure@@ - individual image. Analyzing just an image. </p>
<p>@@Macro-semiotic: the image in context. Thinking about the juxtapositions. You think about the image in contrast to the other images. Doesn't have to be panels next to it, can be last panel of the book. </p>
<span class="ex">2. Batman punching Joker. 1. Joker raised hands surrendering. 1 affects 2. </span>

<h1>Perspective</h1>
<p>Where you place your camera is important in telling them how to feel. Look up at them. Look down at them. <span class="ex">Maus - like youre in the crowd, dreading your turn.</span><span class="ex">Louis Riel. Trial of Louis Riel. Watching from the side. You're in the jury. The author wants you to judge him.</span></p>
<ul>
	<li>Distance</li>
	<li>Eye contact - can they see you or not see you</li>
	<li>Panel Size/Arrangement. The larger the panel, th emore important it is. Rhythmic effect - small panels building to a large panel. </li>
	<li>Quality of Perception - You can only focus on foreground or background. Comics can have both. They can tell you wher eto look by blurring.</li>
	<li>Font - Comic Sans is based on Watchmen lettering. </li>
</ul>

<h1>Serializaiton</h1>
<p>Serialization: a story with a beginning, but no end. Comics can't be like novels. Too expensive. Early days of the novel - had to serialize. Mandate of each issues is to ensure next. Force syou to wrap something up. Sitcoms need resolutions. Limits artists to edit text as a whole. Novelists needs editing. You only get a first draft. You can't discover the story while you're writing it. Restrictions.</p>

