<h1>History of Comics</h1>

<h2>In the beginning</h2>
<p>Telling stories through sequential images is really old. Older than literature. Cave drawings. Comics are a natural art form. People just do it, don't have to learn it. Language is artificial. </p>

<h2>Peripheral Existence</h2>
<p>Begins with Rodolphe Topffer (1799-1846). The father of comics. 6 panel layout in which people would be doing visually and underneath they would have text describing what was going on. 1 page story. It's a picture book in one page. Good comic book - no redundancy between text and image.</p>
<p>Most visible use: ##Political cartoons##. </p>
<p>William Blake - experiments. Wood cuts. Chisel image on wood and use as stamp. Songs of Innocence. Poems with paintings surrounding them. The pictures carry a great deal of the meaning. </p>
<p>Early uses for comics was an underground culture of pornography. Any visual medium is used for pornography. Entrenches certain perceptions of humans. Buried history - we don't have original comics because they were pornographic and illegal. </p>

<h2>The Boom</h2>
<p>Joseph Pilitzer and William Randolph Hearst were in a war for readership on their nespaper. Pulitzer debuted a comics page. Yellow-Shirt Kid. Talks with a card or his shirt talks. Hearst stole the artist. Pulitzer hired another person to draw the Yellow-Shirt Kid. ##Yellow Journalism## - journalism that tries to one-up the competition. </p>

<h2>From there...</h2>
<p>it exploded. Class becomes an issue. Graphic novels. Superhero genre. Comic Strips. Romance comics - aimed for women. Comics got censored in the 1950s. Comics got hijacked because they were corrupting youth. </p>

