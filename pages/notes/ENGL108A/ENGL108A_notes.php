<h1>Representation and Selection</h1>
<p>Trying to convey a piece of a long line of images. Choosing one image to represent a lot of other images. <span class="ex">NBA logo. Best angle and image. </span> Choose which one is best of representing something. What happens in between, we assume. ##Motion## and ##Caricature##. Image captures everything it is. ##CAricature## Choice to include eyes, nose, ears, etc. Stephen Harper's nose. Justin Trudeau - hair. Reducing drawings to have their most representative features.</p>

<h1>Gutter</h1>
<p>Gutter - space between panels of the comic. Between text (no images) and film (all imageS). Construct some images. Things happen between panels. Your brain fills in what happened. The reader animates what happens in comics. </p>

<h1>Semiotic Structures</h1>
<p>@@Micro-semiotic structure@@ - individual image. Analyzing just an image. </p>
<p>@@Macro-semiotic: the image in context. Thinking about the juxtapositions. You think about the image in contrast to the other images. Doesn't have to be panels next to it, can be last panel of the book. </p>
<span class="ex">2. Batman punching Joker. 1. Joker raised hands surrendering. 1 affects 2. </span>

